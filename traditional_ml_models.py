from sklearn.feature_extraction.text import TfidfVectorizer
import scipy.sparse as sp

# Reload raw text for TF-IDF analysis
CSV_PATH = '/content/drive/MyDrive/comparative-analysis-main/datasetnew/'

train_df = pd.read_csv(CSV_PATH + 'train_binary.csv')
test_df  = pd.read_csv(CSV_PATH + 'test_binary.csv')
val_df   = pd.read_csv(CSV_PATH + 'val_binary.csv')

# ── What TF-IDF does ────────────────────────────────────────
# TF  (Term Frequency)     = how often a word appears in ONE article
# IDF (Inverse Doc Freq)   = penalizes words that appear in ALL articles
# TF-IDF = TF × IDF → gives high score to words unique to that article

# ── Fit TF-IDF on training text ─────────────────────────────
tfidf = TfidfVectorizer(
    max_features = 14643,   # match your .npz shape exactly
    ngram_range  = (1, 2),  # unigrams + bigrams
    sublinear_tf = True,    # apply log(1+tf) smoothing
    min_df       = 2        # ignore words appearing in < 2 docs
)

X_train_text = train_df['statement'].astype(str)
X_test_text  = test_df['statement'].astype(str)

X_tfidf_train = tfidf.fit_transform(X_train_text)
X_tfidf_test  = tfidf.transform(X_test_text)

print("TF-IDF Matrix Info:")
print(f"  Training matrix shape : {X_tfidf_train.shape}")
print(f"  Test matrix shape     : {X_tfidf_test.shape}")
print(f"  Total features (words): {len(tfidf.get_feature_names_out())}")
print(f"  Matrix density        : {X_tfidf_train.nnz / (X_tfidf_train.shape[0] * X_tfidf_train.shape[1]):.4f}")
print(f"  (Very low density = sparse matrix, most values are 0)")

# ── Top TF-IDF words overall ─────────────────────────────────
feature_names = tfidf.get_feature_names_out()
mean_tfidf    = np.asarray(X_tfidf_train.mean(axis=0)).flatten()
top_indices   = mean_tfidf.argsort()[-20:][::-1]

print("\nTop 20 most important words by TF-IDF score:")
print("-" * 40)
for i, idx in enumerate(top_indices):
    print(f"  {i+1:2}. {feature_names[idx]:<25} score: {mean_tfidf[idx]:.4f}")

# ── Top words for FAKE news vs REAL news ─────────────────────
y_train = train_df['label'].values

fake_indices = np.where(y_train == 1)[0]
real_indices = np.where(y_train == 0)[0]

fake_tfidf = np.asarray(X_tfidf_train[fake_indices].mean(axis=0)).flatten()
real_tfidf = np.asarray(X_tfidf_train[real_indices].mean(axis=0)).flatten()

top_fake = fake_tfidf.argsort()[-15:][::-1]
top_real = real_tfidf.argsort()[-15:][::-1]

print("\nTop 15 words in FAKE news:")
print("-" * 35)
for idx in top_fake:
    print(f"  {feature_names[idx]:<25} {fake_tfidf[idx]:.4f}")

print("\nTop 15 words in REAL news:")
print("-" * 35)
for idx in top_real:
    print(f"  {feature_names[idx]:<25} {real_tfidf[idx]:.4f}")

# ── Visualization: Top TF-IDF words ──────────────────────────
fig, axes = plt.subplots(1, 2, figsize=(16, 6))

# Fake news top words
