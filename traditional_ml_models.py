import time

# Step 1: Re-run models with timing measurement
timed_results = []

for name, model in models.items():
    print(f"Timing: {name}...")

    # Measure training time
    start_train = time.time()
    model.fit(X_train, y_train)
    train_time = time.time() - start_train

    # Measure inference time
    start_infer = time.time()
    y_pred = model.predict(X_test)
    infer_time = time.time() - start_infer

    # Metrics
    f1  = f1_score(y_test, y_pred, average='weighted', zero_division=0)

    try:
        if hasattr(model, 'predict_proba'):
            y_prob = model.predict_proba(X_test)[:, 1]
        else:
            y_prob = model.decision_function(X_test)
        auc = roc_auc_score(y_test, y_prob)
    except:
        auc = 0.0

    timed_results.append({
        'Model'       : name,
        'F1'          : round(f1, 4),
        'AUC'         : round(auc, 4),
        'Train_Time_s': round(train_time, 4),
        'Infer_Time_s': round(infer_time, 4)
    })

    print(f"  Train time : {train_time:.4f}s")
    print(f"  Infer time : {infer_time:.4f}s")

timed_df = pd.DataFrame(timed_results)
print("\nTiming Results:")
print(timed_df.to_string(index=False))

# Step 2: Calculate FNDS
# Formula: FNDS = α × F1 + β × AUC + γ × (1 - Normalized_Train_Cost)
# Weights: α=0.5, β=0.3, γ=0.2

alpha = 0.5   # F1 weight      - most important (classification balance)
beta  = 0.3   # AUC weight     - discrimination ability
gamma = 0.2   # Cost weight    - rewards faster/cheaper models

# Normalize training time (0 to 1 scale)
max_train_time = timed_df['Train_Time_s'].max()
timed_df['Norm_Cost'] = timed_df['Train_Time_s'] / max_train_time

# Calculate FNDS
timed_df['FNDS'] = (
    alpha * timed_df['F1'] +
    beta  * timed_df['AUC'] +
    gamma * (1 - timed_df['Norm_Cost'])
)
timed_df['FNDS'] = timed_df['FNDS'].round(4)

print("\n" + "="*70)
print("FNDS - Fake News Detection Utility Score")
print(f"Formula: FNDS = {alpha}×F1 + {beta}×AUC + {gamma}×(1 - Norm_Cost)")
print("="*70)
print(timed_df[['Model','F1','AUC','Train_Time_s',
                 'Norm_Cost','FNDS']].to_string(index=False))

# Best model by FNDS
best_fnds = timed_df.loc[timed_df['FNDS'].idxmax(), 'Model']
best_acc  = timed_df.loc[timed_df['F1'].idxmax(), 'Model']
print(f"\nBest model by F1    : {best_acc}")
print(f"Best model by FNDS  : {best_fnds}")
print("(If different, cost-efficiency changes the ranking!)")

