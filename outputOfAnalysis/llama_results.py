def llama_batch_predict(texts):
    prompt = "You are a fake news classifier.\n"
    prompt += "Label each statement as 0 (Real) or 1 (Fake).\n\n"

    for i, text in enumerate(texts):
        prompt += f"{i+1}. {text}\n"

    prompt += "\nOutput only labels in order separated by commas (e.g., 0,1,0).\n"

    response = client.chat.completions.create(
        model="llama-3.1-8b-instant",
        messages=[{"role": "user", "content": prompt}],
        temperature=0
    )

    output = response.choices[0].message.content.strip()

    preds = [int(x) for x in output.replace(" ", "").split(",") if x in ["0","1"]]

    return preds

def llama_predict(text):
    prompt = f"""
    You are a fake news classifier.

    Classify the statement as:
    0 = Real News
    1 = Fake News

    Examples:
    Statement: The earth revolves around the sun.
    Label: 0

    Statement: Drinking bleach cures diseases.
    Label: 1

    Now classify:

    Statement: {text}
    Label:
    """

    response = client.chat.completions.create(
        model="llama-3.1-8b-instant",
        messages=[{"role": "user", "content": prompt}],
        temperature=0
    )

    output = response.choices[0].message.content.strip()

    # Clean output
    return int(output[0]) if output[0] in ["0", "1"] else 0

import time

start_time = time.time()

batch_size = 8
all_preds = []

for i in range(0, len(X_test), batch_size):
    batch = X_test[i:i+batch_size]

    try:
        preds = llama_batch_predict(batch)
    except:
        preds = [0]*len(batch)   # fallback if error

    # Safety fix
    if len(preds) != len(batch):
        preds = preds + [0]*(len(batch) - len(preds))

    all_preds.extend(preds)

    time.sleep(0.5)   # 🔥 IMPORTANT (adjust if needed)

end_time = time.time()
inference_time = end_time - start_time

print("Inference Time:", inference_time)

"""printing metrics result"""

